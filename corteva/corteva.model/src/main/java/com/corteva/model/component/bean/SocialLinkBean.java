package com.corteva.model.component.bean;



/**
 * The is the Social Link bean class.
 * 
 * @author Sapient
 */
public class SocialLinkBean {

	/** The social link label. */
	private String iconLabel;

	/** The social image name. */
	private String iconImage;
	
	/** The social icon alt. */
	private String iconAlt;

	/**
	 * Gets the icon alt.
	 *
	 * @return the icon alt
	 */
	public String getIconAlt() {
		return iconAlt;
	}

	/**
	 * Sets the icon alt.
	 *
	 * @param iconAlt the new icon alt
	 */
	public void setIconAlt(String iconAlt) {
		this.iconAlt = iconAlt;
	}

	/**
	 * Gets the icon image.
	 *
	 * @return the icon image
	 */
	public String getIconImage() {
		return iconImage;
	}

	/**
	 * Sets the icon image.
	 *
	 * @param iconImage the new icon image
	 */
	public void setIconImage(String iconImage) {
		this.iconImage = iconImage;
	}

	/** The social link node name. */
	private String iconLink;

	/**
	 * Gets the icon label.
	 *
	 * @return the icon label
	 */
	public String getIconLabel() {
		return iconLabel;
	}

	/**
	 * Sets the icon label.
	 *
	 * @param iconLabel
	 *            the new icon label
	 */
	public void setIconLabel(String iconLabel) {
		this.iconLabel = iconLabel;
	}

	

	/**
	 * Gets the icon link.
	 *
	 * @return the icon link
	 */
	public String getIconLink() {
		return iconLink;
	}

	/**
	 * Sets the icon link.
	 *
	 * @param iconLink
	 *            the new icon link
	 */
	public void setIconLink(String iconLink) {
		this.iconLink = iconLink;
	}

}
