package com.corteva.model.component.bean;



/**
 * The Class LanguageBean.
 */
public class LanguageBean {
	
	/** The language name. */
	private String languageName;
	
	/** The language title. */
	private String languageTitle;
	
	/** The language code. */
	private String languageCode;
	
	/** The language link. */
	private String languageLink;

	/**
	 * Gets the language name.
	 *
	 * @return the language name
	 */
	public String getLanguageName() {
		return languageName;
	}

	/**
	 * Sets the language name.
	 *
	 * @param languageName the new language name
	 */
	public void setLanguageName(String languageName) {
		this.languageName = languageName;
	}

	/**
	 * Gets the language title.
	 *
	 * @return the language title
	 */
	public String getLanguageTitle() {
		return languageTitle;
	}

	/**
	 * Sets the language title.
	 *
	 * @param languageTitle the new language title
	 */
	public void setLanguageTitle(String languageTitle) {
		this.languageTitle = languageTitle;
	}

	/**
	 * Gets the language code.
	 *
	 * @return the language code
	 */
	public String getLanguageCode() {
		return languageCode;
	}

	/**
	 * Sets the language code.
	 *
	 * @param languageCode the new language code
	 */
	public void setLanguageCode(String languageCode) {
		this.languageCode = languageCode;
	}

	/**
	 * Gets the language link.
	 *
	 * @return the language link
	 */
	public String getLanguageLink() {
		return languageLink;
	}

	/**
	 * Sets the language link.
	 *
	 * @param languageLink the new language link
	 */
	public void setLanguageLink(String languageLink) {
		this.languageLink = languageLink;
	}
	
	

}
