package com.corteva.model.component.bean;

/**
 * The is the bean class that contains a String field that is used to set and
 * get the experience fragment path value.
 * 
 * @author Sapient
 */
public class ExperienceFragmentBean {

	/** The expfrag path. */
	private String expfragPath;

	/**
	 * Gets the expfrag path.
	 *
	 * @return the expfrag path
	 */
	public String getExpfragPath() {
		return expfragPath;
	}

	/**
	 * Sets the expfrag path.
	 *
	 * @param expfragPath
	 *            the new expfrag path
	 */
	public void setExpfragPath(String expfragPath) {
		this.expfragPath = expfragPath;
	}

}
